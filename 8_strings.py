## String manipulation

# operations on strings
s1 = "hello"
print(s1)

s2 = "hello".capitalize()
print(s2)

s3 = "hello".upper()
print(s3)

# Multiline strings
line = """This is a sample string

which has many lines

like this."""
print(line)

# format string
s = "My name is {}".format("Teja")
print(s)

# format string shortcut
num = 5
s = f"Number: {num}"
print(s)
